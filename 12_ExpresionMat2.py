#Expresion matermatica 
a = float(input("Ingresa el elemento a: "))
b = float(input("Ingresa el elemento b: "))
c = float(input("Ingresa el elemento c: "))
d = float(input("Ingresa el elemento d: "))
e = float(input("Ingresa el elemento e: "))
f = float(input("Ingresa el elemento f: "))
sol = (a+ (b/c))/(d+(e/f))
print(f"La solución de la expresión es: {sol:,.3f}")