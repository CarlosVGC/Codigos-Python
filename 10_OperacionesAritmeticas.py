# Programa donde se realizan diferentes tipos de operaciones

n1 = float(input("Ingresa el primer numero: "))
n2 = float(input("Ingresa el primer numero: "))

suma = n1 + n2
resta = n1 - n2
mult = n1 * n2
div = n1 / n2

print(f"El resultado de las operaciones es:\nsuma:  {suma}\nresta: {resta}\nmult: {mult}\ndiv: {div: 1.3} ")
